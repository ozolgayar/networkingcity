/* ----------------------------------------------------------------
   initIntro()
   Пошаговое интро: Злата объясняет правила.
   Шаги берутся из массива INTRO_STEPS выше.
   ---------------------------------------------------------------- */
function initIntro() {
  let currentStep = 0;

  const bubbleText = document.getElementById('intro-bubble-text');
  const dotsWrap   = document.getElementById('intro-dots');
  const btnNext    = document.getElementById('btn-intro-next');

  // Создаём точки прогресса
  dotsWrap.innerHTML = '';
  INTRO_STEPS.forEach((_, i) => {
    const dot = document.createElement('div');
    dot.className = 'intro-dot' + (i === 0 ? ' active' : '');
    dot.dataset.step = i;
    dotsWrap.appendChild(dot);
  });

  // Функция рендера шага
  function renderStep(idx) {
    // Анимация смены текста
    bubbleText.style.opacity = '0';
    bubbleText.style.transform = 'translateY(6px)';

    setTimeout(() => {
      bubbleText.textContent = INTRO_STEPS[idx];
      bubbleText.style.transition = 'all 0.25s ease';
      bubbleText.style.opacity = '1';
      bubbleText.style.transform = 'translateY(0)';
    }, 150);

    // Обновляем точки
    dotsWrap.querySelectorAll('.intro-dot').forEach((dot, i) => {
      dot.classList.remove('active', 'done');
      if (i < idx)  dot.classList.add('done');
      if (i === idx) dot.classList.add('active');
    });

    // Последний шаг — меняем текст кнопки
    btnNext.textContent = idx === INTRO_STEPS.length - 1
      ? 'Начать!'
      : 'Далее →';
  }

  // Кнопка «Далее»
  btnNext.onclick = function() {
    if (currentStep < INTRO_STEPS.length - 1) {
      currentStep++;
      renderStep(currentStep);
    } else {
      // Интро закончено — переходим на хаб
      showM2Screen('m2-hub');
      refreshHubZones();
    }
  };

  // Показываем первый шаг
  renderStep(0);
}


