/* ----------------------------------------------------------------
   spendEnergy(amount)
   Тратит amount% энергии.
   Если энергия падает до 0 — показывает предупреждение.
   ---------------------------------------------------------------- */
function spendEnergy(amount) {
  m2State.energy = Math.max(0, m2State.energy - amount);
  updateHUD();
  saveProgress();

  // Если энергия критически низкая
  if (m2State.energy <= 10 && m2State.energy > 0) {
    showToast('⚡ Энергия на исходе! Найди стол с едой', 'warning');
  }

  // Если энергия закончилась совсем
  if (m2State.energy <= 0) {
    showToast('⚡ Энергия на нуле!', 'error');
  }
}

/* ----------------------------------------------------------------
   startRefuel(zoneId)
   Запускает экран пополнения энергии.
   ---------------------------------------------------------------- */
function startRefuel(zoneId) {
  const zone        = ENERGY_ZONES[zoneId];
  const currentPct  = m2State.energy;
  const gainPct     = zone.energyGain;
  const newPct      = Math.min(100, currentPct + gainPct);

  // Заполняем экран
  document.getElementById('refuel-title').textContent  = zone.title;
  document.getElementById('refuel-desc').textContent   = zone.desc;

  // Полоска — начальное значение (текущая энергия)
  const barEl = document.getElementById('refuel-energy-bar');
  barEl.style.width = currentPct + '%';

  document.getElementById('refuel-energy-text').textContent =
    currentPct + '% → ' + newPct + '%';

  showM2Screen('m2-refuel');

  // Анимируем полоску до нового значения
  setTimeout(() => {
    barEl.style.width = newPct + '%';
  }, 300);

  // Кнопка «Продолжить»
  const continueBtn = document.getElementById('btn-refuel-continue');
  const newBtn = continueBtn.cloneNode(true);
  continueBtn.parentNode.replaceChild(newBtn, continueBtn);

  newBtn.addEventListener('click', function() {
    // Применяем пополнение
    m2State.energyZoneUses[zoneId] =
      (m2State.energyZoneUses[zoneId] || 0) + 1;
    m2State.energy = newPct;

    updateHUD();
    saveProgress();
    refreshHubZones();

    showToast('⚡ Энергия восстановлена!', 'success');

    // Возвращаемся на хаб или в главу
    if (m2State.refuelReturnChapterId) {
      const chId = m2State.refuelReturnChapterId;
      m2State.refuelReturnChapterId = null;
      startChapter(chId);
    } else {
      showM2Screen('m2-hub');
      refreshHubZones();
    }
  });
}


