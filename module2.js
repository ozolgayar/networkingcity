/* ----------------------------------------------------------------
   startChapter(chapterId)
   Инициализирует прохождение главы:
   устанавливает текущую главу и запускает первую ситуацию.
   ---------------------------------------------------------------- */
function startChapter(chapterId) {
  const chapter = CHAPTERS[chapterId];
  if (!chapter) return;

  // Сохраняем текущую главу в состояние
  m2State.currentChapterId    = chapterId;
  m2State.currentSituationIdx = 0;

// Переходим на экран диалога
  showM2Screen('m2-dialog');

  // Рендерим первую ситуацию
  showSituation(chapter, 0);
}

/* ----------------------------------------------------------------
   showSituation(chapter, idx)
   Рендерит одну ситуацию (сцену + вопрос + варианты ответа).
   Вызывается при старте главы и после каждого ответа.
   ---------------------------------------------------------------- */
function showSituation(chapter, idx) {
  const situation = chapter.situations[idx];
  if (!situation) return;
   
   const dialogScreen = document.getElementById('m2-dialog');
  if (dialogScreen) {
    if (chapter.dialogBg) {
      dialogScreen.style.setProperty('--m2-dialog-bg', `url("${chapter.dialogBg}")`);
    } else {
      dialogScreen.style.setProperty('--m2-dialog-bg', 'none');
    }
  }

  // Сбрасываем флаг подсказки
  m2State.hintUsedInSituation = false;

  // ── Заголовок диалога ────────────────────────────────────────
  document.getElementById('dialog-theme-label').textContent = chapter.theme;

  // Счётчик ситуаций
  const counterEl = document.getElementById('dialog-situation-counter');
  if (chapter.showCounter) {
    counterEl.textContent =
      'Ситуация ' + (idx + 1) + ' / ' + chapter.situations.length;
    counterEl.style.display = '';
  } else {
    counterEl.style.display = 'none';
  }

  // ── NPC ─────────────────────────────────────────────────────
  const npc = chapter.npc;

  // Иконка или фото
  const avatarIcon = document.getElementById('dialog-npc-avatar-icon');
  const avatarImg  = document.getElementById('dialog-npc-avatar-img');

  if (npc.photo) {
    // Если есть фото — показываем его, скрываем иконку
    avatarImg.src = npc.photo;
    avatarImg.style.display = 'block';
    avatarIcon.style.display = 'none';
  } else {
    // Иначе — эмодзи-заглушка
    avatarIcon.textContent   = npc.icon;
    avatarIcon.style.display = 'block';
    avatarImg.style.display  = 'none';
  }

  document.getElementById('dialog-npc-name').textContent = npc.name;
  document.getElementById('dialog-npc-role').textContent = npc.role;

  // Сбрасываем рамку аватара и эмодзи реакции
  const avatarEl = document.getElementById('dialog-npc-avatar');
  avatarEl.classList.remove('answer-correct', 'answer-wrong', 'answer-partial');

  const emojiEl = document.getElementById('dialog-npc-emoji');
  emojiEl.textContent = '';
  emojiEl.classList.remove('visible');

  // ── Текст сцены ─────────────────────────────────────────────
  document.getElementById('dialog-scene-text').textContent = situation.scene;

  // ── Зона ответов ─────────────────────────────────────────────
  const answersZone = document.getElementById('dialog-answers-zone');
  answersZone.innerHTML = '';

  // Вопрос
  const questionEl = document.createElement('div');
  questionEl.className   = 'dialog-question';
  questionEl.textContent = situation.question;
  answersZone.appendChild(questionEl);

  // Контейнер вариантов
  const optionsEl = document.createElement('div');
  optionsEl.className = 'dialog-options';
  answersZone.appendChild(optionsEl);

  // Буквы для вариантов
  const letters = ['А', 'Б', 'В', 'Г', 'Д'];

  // ── Перемешиваем варианты ответа (Fisher-Yates shuffle) ──
  // Сохраняем оригинальный индекс каждого варианта,
  // чтобы handleAnswer получил правильный option из situation.options
  const shuffledOptions = situation.options
    .map((opt, originalIdx) => ({ opt, originalIdx }))
    .sort(() => Math.random() - 0.5);

  shuffledOptions.forEach(({ opt, originalIdx }, displayIdx) => {
    const btn = document.createElement('button');
    btn.className = 'dialog-option';

    btn.innerHTML = `
      <div class="dialog-option-letter">${letters[displayIdx]}</div>
      <div class="dialog-option-text">${opt.text}</div>
    `;

    // Обработчик клика — передаём ОРИГИНАЛЬНЫЙ индекс
    btn.addEventListener('click', function() {
      handleAnswer(chapter, idx, originalIdx, situation);
    });

    optionsEl.appendChild(btn);
  });

  // Разделитель перед подсказкой
  const divider = document.createElement('div');
  divider.className = 'dialog-divider';
  answersZone.appendChild(divider);

  // Кнопка подсказки
  const hintBtn = document.createElement('button');
  hintBtn.className = 'dialog-hint-btn';
  hintBtn.id        = 'dialog-hint-btn';
  hintBtn.innerHTML = `
    💡 Подсказка
    <span class="dialog-hint-cost">(-10% ⚡)</span>
  `;

  hintBtn.addEventListener('click', function() {
    handleHint(situation, hintBtn);
  });

  answersZone.appendChild(hintBtn);

  // Прокручиваем наверх зоны ответов
  answersZone.scrollTop = 0;
}

/* ----------------------------------------------------------------
   handleAnswer(chapter, situationIdx, optionIdx, situation)
   Обрабатывает выбор варианта ответа:
   1. Блокирует все кнопки
   2. Подсвечивает выбранный вариант
   3. Показывает реакцию NPC (эмодзи + текст)
   4. Тратит энергию если ответ неверный
   5. Показывает кнопки «Попробовать снова» или «Дальше»
   ---------------------------------------------------------------- */
function handleAnswer(chapter, situationIdx, optionIdx, situation) {
  const option      = situation.options[optionIdx];
  const answersZone = document.getElementById('dialog-answers-zone');
  const allOptions  = answersZone.querySelectorAll('.dialog-option');
  const avatarEl    = document.getElementById('dialog-npc-avatar');
  const emojiEl     = document.getElementById('dialog-npc-emoji');

  // ── Блокируем все кнопки вариантов ──────────────────────────
// Сначала находим индекс кликнутой кнопки по тексту варианта
  let pickedBtnIdx = -1;
  allOptions.forEach((btn, i) => {
    const textEl = btn.querySelector('.dialog-option-text');
    if (textEl && textEl.textContent.trim() === option.text.trim()) {
      pickedBtnIdx = i;
    }
  });

  allOptions.forEach((btn, i) => {
    btn.disabled = true;
    if (i !== pickedBtnIdx) {
      btn.classList.add('dimmed');
    }
  });

  // ── Скрываем кнопку подсказки ────────────────────────────────
  const hintBtn = document.getElementById('dialog-hint-btn');
  if (hintBtn) hintBtn.style.display = 'none';

   // ── Подсвечиваем выбранный вариант ──────────────────────────
  // Ищем кнопку по тексту варианта (т.к. порядок перемешан)
  let clickedBtnIdx = -1;
  allOptions.forEach((btn, i) => {
    const textEl = btn.querySelector('.dialog-option-text');
    if (textEl && textEl.textContent.trim() === option.text.trim()) {
      clickedBtnIdx = i;
    }
  });
  if (clickedBtnIdx >= 0) {
    allOptions[clickedBtnIdx].classList.add(option.type);
  }
  // type = 'correct' | 'partial' | 'wrong'

  // ── Реакция аватара: рамка + эмодзи ─────────────────────────
  avatarEl.classList.add('answer-' + option.type);

  // Показываем эмодзи с анимацией
  emojiEl.textContent = option.emoji;
  // Небольшая задержка чтобы CSS-анимация сработала
  requestAnimationFrame(() => {
    requestAnimationFrame(() => emojiEl.classList.add('visible'));
  });

  // ── Тратим энергию при неверном/частичном ответе ────────────
  if (option.type === 'wrong') {
    spendEnergy(15);
  } else if (option.type === 'partial') {
    spendEnergy(5);
  }

  // ── Встряска при неверном ответе ────────────────────────────
  if (option.type === 'wrong') {
    const npcZone = document.querySelector('.dialog-npc-zone');
    npcZone.classList.add('m2-shake');
    setTimeout(() => npcZone.classList.remove('m2-shake'), 500);
  }

  // ── Пульсация при верном ответе ─────────────────────────────
  if (option.type === 'correct') {
    avatarEl.classList.add('m2-success-pulse');
    setTimeout(() => avatarEl.classList.remove('m2-success-pulse'), 700);

    // Анимируем счётчик знакомств в HUD
    const meetsEl = document.getElementById('hud-meets-value');
    if (meetsEl) meetsEl.classList.add('m2-counter-pop');
    setTimeout(() => {
      if (meetsEl) meetsEl.classList.remove('m2-counter-pop');
    }, 500);
  }

  // ── Блок реакции NPC ─────────────────────────────────────────
  // Удаляем старый блок реакции если есть
  const oldReaction = answersZone.querySelector('.dialog-reaction');
  if (oldReaction) oldReaction.remove();

  // Иконка реакции по типу ответа
  const reactionIcons = {
    correct: '✅',
    partial: '🟡',
    wrong:   '❌',
  };

  const reactionEl = document.createElement('div');
  reactionEl.className = 'dialog-reaction';
  reactionEl.innerHTML = `
    <div style="display:flex; align-items:center; gap:8px;">
      <span class="dialog-reaction-icon">${reactionIcons[option.type]}</span>
      <span class="dialog-reaction-text">${option.reaction}</span>
    </div>
  `;

  // ── Кнопки после ответа ──────────────────────────────────────

  // «Попробовать снова» — только при неверном ответе
  if (option.type === 'wrong') {
    const retryBtn = document.createElement('button');
    retryBtn.className = 'm2-btn m2-btn-secondary dialog-retry-btn';
    retryBtn.textContent = '← Попробовать снова';
    retryBtn.addEventListener('click', function() {
      // Перерисовываем ту же ситуацию
      showSituation(chapter, situationIdx);
    });
    reactionEl.appendChild(retryBtn);
  }

  // «Дальше» — при верном или частичном ответе
  if (option.type === 'correct' || option.type === 'partial') {
    const nextBtn = document.createElement('button');
    nextBtn.className = 'm2-btn m2-btn-primary dialog-next-btn';

    const isLastSituation =
      situationIdx >= chapter.situations.length - 1;

    nextBtn.textContent = isLastSituation
      ? 'Завершить знакомство →'
      : 'Следующая ситуация →';

    nextBtn.addEventListener('click', function() {
      if (isLastSituation) {
        // Глава завершена
        finishChapter(chapter);
      } else {
        // Следующая ситуация
        m2State.currentSituationIdx = situationIdx + 1;
        showSituation(chapter, situationIdx + 1);
      }
    });

    reactionEl.appendChild(nextBtn);
  }

  answersZone.appendChild(reactionEl);
 // Мягко прокручиваем ТОЛЬКО внутри зоны ответов, без сдвига всего экрана
  setTimeout(() => {
    answersZone.scrollTo({
      top: answersZone.scrollHeight,
      behavior: 'smooth'
    });
  }, 200);
}

/* ----------------------------------------------------------------
   handleHint(situation, hintBtn)
   Показывает подсказку в модалке и тратит 10% энергии.
   Одна подсказка на ситуацию — после использования кнопка
   становится неактивной.
   ---------------------------------------------------------------- */
function handleHint(situation, hintBtn) {
  // Уже использована подсказка в этой ситуации?
  if (m2State.hintUsedInSituation) {
    showToast('Подсказка уже использована', 'warning');
    return;
  }

  // Открываем модалку подтверждения
  document.getElementById('hint-modal-text').textContent = situation.hint;
  openModal('modal-hint-confirm');

  // Кнопка «Использовать» внутри модалки
  const useBtn = document.getElementById('btn-use-hint');

  // Удаляем старый обработчик чтобы не накапливались
  const newUseBtn = useBtn.cloneNode(true);
  useBtn.parentNode.replaceChild(newUseBtn, useBtn);

  newUseBtn.addEventListener('click', function() {
    closeModal('modal-hint-confirm');

    // Тратим энергию
    spendEnergy(10);

    // Помечаем подсказку как использованную
    m2State.hintUsedInSituation = true;
    hintBtn.classList.add('used');
    hintBtn.innerHTML = '💡 Подсказка использована';

    showToast('Подсказка открыта (-10% ⚡)', 'warning');
  });
}

/* ----------------------------------------------------------------
   finishChapter(chapter)
   Завершает главу:
   1. Добавляет главу в completedChapters
   2. Увеличивает счётчик знакомств
   3. Разблокирует артефакт
   4. Сохраняет прогресс
   5. Показывает экран конца главы
   ---------------------------------------------------------------- */
function finishChapter(chapter) {
  // Не считаем дважды
  if (!m2State.completedChapters.includes(chapter.id)) {
    m2State.completedChapters.push(chapter.id);
    m2State.meets++;

    // Разблокируем артефакт
    if (chapter.artifact) {
      unlockArtifact(chapter.artifact);
    }

    // Сохраняем прогресс
    saveProgress();

    // Обновляем HUD
    updateHUD();
  }

  // Показываем экран конца главы
  showChapterEnd(chapter);
}


