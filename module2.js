/* ----------------------------------------------------------------
   showM2Screen(screenId)
   Переключает активный экран.
   Скрывает HUD на обложке и интро, показывает на остальных.
   ---------------------------------------------------------------- */
function showM2Screen(screenId) {
  // Убираем класс active у всех экранов
  document.querySelectorAll('.m2-screen').forEach(s => s.classList.remove('active'));

  // Показываем нужный экран
  const target = document.getElementById(screenId);
  if (target) target.classList.add('active');

  // HUD скрыт на обложке и интро
  const hud = document.getElementById('m2-hud');
  if (screenId === 'm2-cover' || screenId === 'm2-intro') {
    hud.classList.add('hidden');
  } else {
    hud.classList.remove('hidden');
    updateHUD();
  }

  // Помечаем body классом текущего экрана —
  // чтобы в CSS можно было стилизовать HUD по-разному
  document.body.classList.remove(
    'm2-on-cover','m2-on-intro','m2-on-hub',
    'm2-on-dialog','m2-on-refuel','m2-on-chapter-end','m2-on-final'
  );
  document.body.classList.add('m2-on-' + screenId.replace('m2-',''));
}

/* ----------------------------------------------------------------
   updateHUD()
   Обновляет полоску энергии и счётчик знакомств.
   Меняет цвет полоски в зависимости от уровня энергии:
   > 50% — зелёный, 20–50% — жёлтый, < 20% — красный
   ---------------------------------------------------------------- */
function updateHUD() {
  const pct   = Math.max(0, Math.min(100, m2State.energy));
  const bar   = document.getElementById('hud-energy-bar');
  const value = document.getElementById('hud-energy-value');
  const meets = document.getElementById('hud-meets-value');

  // Ширина полоски
  bar.style.width = pct + '%';

  // Цвет полоски по уровню энергии
  let color;
  if (pct > 50)      color = '#22c55e'; // зелёный
  else if (pct > 20) color = '#f59e0b'; // жёлтый
  else               color = '#ef4444'; // красный

  bar.style.background = color;
  document.documentElement.style.setProperty('--m2-energy-color', color);

  // Текст значения
  value.textContent = pct + '%';
  value.style.color = color;

  // Счётчик знакомств
  meets.textContent = m2State.meets + ' / ' + m2State.totalMeets;
}

/* ----------------------------------------------------------------
   showToast(text, type)
   Показывает всплывающее уведомление снизу.
   type: 'success' | 'warning' | 'error' | '' (нейтральный)
   ---------------------------------------------------------------- */
let toastTimer = null;
function showToast(text, type) {
  // Удаляем старый тост если есть
  const old = document.getElementById('m2-toast-el');
  if (old) old.remove();
  if (toastTimer) clearTimeout(toastTimer);

  const toast = document.createElement('div');
  toast.id = 'm2-toast-el';
  toast.className = 'm2-toast';
  toast.textContent = text;

  // Цвет границы по типу
  const borderColors = {
    success: '#22c55e',
    warning: '#f59e0b',
    error:   '#ef4444',
  };
  if (borderColors[type]) {
    toast.style.borderColor = borderColors[type];
    toast.style.color = borderColors[type];
  }

  document.body.appendChild(toast);

  // Появление
  requestAnimationFrame(() => {
    requestAnimationFrame(() => toast.classList.add('visible'));
  });

  // Автоскрытие через 2.5 секунды
  toastTimer = setTimeout(() => {
    toast.classList.remove('visible');
    setTimeout(() => toast.remove(), 400);
  }, 2500);
}

