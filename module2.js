/* ----------------------------------------------------------------
   unlockArtifact(artifact)
   Разблокирует артефакт и показывает модалку «получен артефакт».
   ---------------------------------------------------------------- */
function unlockArtifact(artifact) {
  // Не добавляем дважды
  if (m2State.artifacts.find(a => a.id === artifact.id)) return;

  m2State.artifacts.push(artifact);
  saveProgress();

  // Заполняем модалку
  document.getElementById('artifact-modal-icon').textContent    = artifact.icon;
  document.getElementById('artifact-modal-name').textContent    = artifact.name;
  document.getElementById('artifact-modal-preview').textContent = artifact.preview;

  openModal('modal-artifact');
}

/* ----------------------------------------------------------------
   renderNotebook()
   Рендерит содержимое блокнота (все артефакты).
   Вызывается при открытии модалки блокнота.
   ---------------------------------------------------------------- */
function renderNotebook() {
  const body = document.getElementById('notebook-body');

  if (m2State.artifacts.length === 0) {
    body.innerHTML = `
      <p style="color:var(--m2-text-muted); text-align:center; padding:20px 0;">
        Блокнот пуст. Пройди главы, чтобы собрать артефакты 📖
      </p>
    `;
    return;
  }

  body.innerHTML = m2State.artifacts.map(artifact => `
    <div class="notebook-artifact-item">
      <div class="notebook-artifact-header">
        <span class="notebook-artifact-icon">${artifact.icon}</span>
        <span class="notebook-artifact-name">${artifact.name}</span>
      </div>
      <div class="notebook-artifact-body">
        ${artifact.content}
      </div>
    </div>
  `).join('');
}

/* ----------------------------------------------------------------
   downloadPdfMemo()
   Генерирует красивую PDF-памятку с поддержкой кириллицы
   через шрифт Roboto.
   ---------------------------------------------------------------- */
async function downloadPdfMemo() {
  if (m2State.artifacts.length === 0) {
    showToast('Сначала собери хотя бы один артефакт', 'warning');
    return;
  }

  if (typeof window.jspdf === 'undefined') {
    showToast('Не удалось загрузить генератор PDF', 'error');
    return;
  }

  // Блокируем кнопку, чтобы не нажали дважды
  const btn = document.getElementById('btn-download-pdf');
  if (btn) {
    btn.disabled = true;
    btn.textContent = '⏳ Готовим PDF…';
  }

  showToast('📄 Загружаем шрифт…', '');

  // Загружаем шрифт Roboto
  const font = await loadRobotoFont();

  if (!font) {
    showToast('Не удалось загрузить шрифт', 'error');
    if (btn) {
      btn.disabled = false;
      btn.textContent = '⬇️ Скачать PDF-памятку';
    }
    return;
  }

  const { jsPDF } = window.jspdf;
  const doc = new jsPDF({
    unit: 'mm',
    format: 'a4',
    orientation: 'portrait',
  });

  // ── Регистрируем шрифт Roboto в jsPDF ────────────────────
  doc.addFileToVFS('Roboto-Regular.ttf', font.regular);
  doc.addFont('Roboto-Regular.ttf', 'Roboto', 'normal');

  doc.addFileToVFS('Roboto-Bold.ttf', font.bold);
  doc.addFont('Roboto-Bold.ttf', 'Roboto', 'bold');

  doc.setFont('Roboto', 'normal');

  // ── Цвета палитры ─────────────────────────────────────────
  const COLOR = {
    text:     [15, 36, 72],
    muted:    [100, 116, 139],
    accent:   [14, 165, 233],
    accent2:  [2, 132, 199],
    success:  [34, 197, 94],
    warning:  [245, 158, 11],
    bgLight:  [240, 249, 255],
    bgCard:   [248, 250, 252],
    border:   [203, 213, 225],
    white:    [255, 255, 255],
  };

  const PAGE_W = 210;
  const PAGE_H = 297;
  const MARGIN = 18;
  const CONTENT_W = PAGE_W - MARGIN * 2;

  let y = 0;

  /* ── Хелпер: фон страницы ──────────────────────────────── */
  function drawPageBg() {
    doc.setFillColor(...COLOR.accent);
    doc.rect(0, 0, PAGE_W, 4, 'F');
  }

  /* ── Хелпер: новая страница ────────────────────────────── */
  function newPage() {
    doc.addPage();
    drawPageBg();
    y = MARGIN + 8;
  }

  /* ── Хелпер: проверка переполнения ─────────────────────── */
  function ensureSpace(needed) {
    if (y + needed > PAGE_H - MARGIN - 10) {
      newPage();
    }
  }

  /* ── Хелпер: разбиение текста на строки ────────────────── */
  function wrapText(text, maxWidth) {
    return doc.splitTextToSize(text || '', maxWidth);
  }

  /* ── Хелпер: очистка HTML ──────────────────────────────── */
  function stripHtml(html) {
    if (!html) return '';
    return html
      .replace(/<li[^>]*>/gi, '• ')
      .replace(/<\/li>/gi, '\n')
      .replace(/<br\s*\/?>/gi, '\n')
      .replace(/<\/p>/gi, '\n')
      .replace(/<\/?strong[^>]*>/gi, '**')
      .replace(/<\/?b[^>]*>/gi, '**')
      .replace(/<\/?ul[^>]*>/gi, '')
      .replace(/<\/?p[^>]*>/gi, '')
      .replace(/<[^>]+>/g, '')
      .replace(/&nbsp;/g, ' ')
      .replace(/&amp;/g, '&')
      .replace(/&quot;/g, '"')
      .replace(/&laquo;/g, '«')
      .replace(/&raquo;/g, '»')
      .replace(/&lt;/g, '<')
      .replace(/&gt;/g, '>')
      .replace(/\n{3,}/g, '\n\n')
      .replace(/[ \t]+/g, ' ')
      .trim();
  }

  /* ── Хелпер: текст с поддержкой **жирных** фрагментов ──── */
  function drawSmartText(rawText, x, maxWidth, lineHeight) {
    // Разбиваем текст на сегменты: обычные и жирные (между **)
    const parts = rawText.split(/(\*\*[^*]+\*\*)/g).filter(Boolean);

    // Если жирных нет — выводим обычно
    if (parts.length === 1 && !parts[0].startsWith('**')) {
      const lines = wrapText(rawText, maxWidth);
      lines.forEach(line => {
        ensureSpace(lineHeight);
        doc.text(line, x, y);
        y += lineHeight;
      });
      return;
    }

    // Иначе строим строки вручную с учётом жирного
    let currentLine = [];
    let currentWidth = 0;

    function flushLine() {
      if (currentLine.length === 0) return;
      ensureSpace(lineHeight);
      let xCursor = x;
      currentLine.forEach(segment => {
        doc.setFont('Roboto', segment.bold ? 'bold' : 'normal');
        doc.text(segment.text, xCursor, y);
        xCursor += doc.getTextWidth(segment.text);
      });
      y += lineHeight;
      currentLine = [];
      currentWidth = 0;
    }

    parts.forEach(part => {
      const isBold = part.startsWith('**') && part.endsWith('**');
      const text = isBold ? part.slice(2, -2) : part;

      doc.setFont('Roboto', isBold ? 'bold' : 'normal');

      // Разбиваем по словам
      const words = text.split(/(\s+)/);
      words.forEach(word => {
        if (!word) return;
        const wordWidth = doc.getTextWidth(word);

        if (currentWidth + wordWidth > maxWidth && currentLine.length > 0) {
          flushLine();
          // Не переносим пробел в начало новой строки
          if (/^\s+$/.test(word)) return;
        }
        currentLine.push({ text: word, bold: isBold });
        currentWidth += wordWidth;
      });
    });

    flushLine();
    doc.setFont('Roboto', 'normal');
  }

  // ════════════════════════════════════════════════════════
  // ── СТРАНИЦА 1: ОБЛОЖКА ────────────────────────────────
  // ════════════════════════════════════════════════════════

  // Голубая шапка
  doc.setFillColor(...COLOR.accent);
  doc.rect(0, 0, PAGE_W, 70, 'F');

  // Заголовок
  doc.setTextColor(...COLOR.white);
  doc.setFont('Roboto', 'bold');
  doc.setFontSize(28);
  doc.text('Памятка нетворкера', PAGE_W / 2, 32, { align: 'center' });

  doc.setFont('Roboto', 'normal');
  doc.setFontSize(13);
  doc.text('Модуль 2 — Конференция', PAGE_W / 2, 46, { align: 'center' });

  doc.setFontSize(10);
  doc.text('Злата учится знакомиться', PAGE_W / 2, 56, { align: 'center' });

  // Основная карточка
  y = 95;
  doc.setFillColor(...COLOR.bgLight);
  doc.setDrawColor(...COLOR.border);
  doc.setLineWidth(0.3);
  doc.roundedRect(MARGIN, y, CONTENT_W, 70, 4, 4, 'FD');

  doc.setTextColor(...COLOR.text);
  doc.setFont('Roboto', 'bold');
  doc.setFontSize(18);
  doc.text(
    `${m2State.artifacts.length} техник знакомства`,
    PAGE_W / 2, y + 22,
    { align: 'center' }
  );

  doc.setFont('Roboto', 'normal');
  doc.setFontSize(11);
  doc.setTextColor(...COLOR.muted);
  doc.text(
    'собранных за время прохождения конференции',
    PAGE_W / 2, y + 35,
    { align: 'center' }
  );

  // Дата
  const today = new Date();
  const dateStr = today.toLocaleDateString('ru-RU', {
    day: 'numeric', month: 'long', year: 'numeric'
  });
  doc.setFontSize(9);
  doc.setTextColor(...COLOR.accent2);
  doc.text(dateStr, PAGE_W / 2, y + 58, { align: 'center' });

  // Оглавление
  y = 185;
  doc.setFont('Roboto', 'bold');
  doc.setFontSize(13);
  doc.setTextColor(...COLOR.text);
  doc.text('Содержание:', MARGIN, y);
  y += 10;

  doc.setFont('Roboto', 'normal');
  doc.setFontSize(11);

  m2State.artifacts.forEach((art, i) => {
    // Номер в кружке
    doc.setFillColor(...COLOR.accent);
    doc.circle(MARGIN + 3, y - 1.5, 3, 'F');
    doc.setTextColor(...COLOR.white);
    doc.setFont('Roboto', 'bold');
    doc.setFontSize(8);
    doc.text(String(i + 1), MARGIN + 3, y + 0.5, { align: 'center' });

    // Название
    doc.setFont('Roboto', 'normal');
    doc.setFontSize(11);
    doc.setTextColor(...COLOR.text);
    doc.text(art.name || 'Без названия', MARGIN + 10, y + 0.5);
    y += 9;
  });

  // Подпись внизу
  doc.setFont('Roboto', 'normal');
  doc.setFontSize(9);
  doc.setTextColor(...COLOR.muted);
  doc.text(
    'Сохрани эту памятку и используй на следующей конференции',
    PAGE_W / 2, PAGE_H - 20,
    { align: 'center' }
  );

  // ════════════════════════════════════════════════════════
  // ── СТРАНИЦЫ С АРТЕФАКТАМИ ─────────────────────────────
  // ════════════════════════════════════════════════════════

  m2State.artifacts.forEach((artifact, idx) => {
    newPage();

    // Шапка артефакта: цветной блок с номером и названием
    const headerH = 28;
    doc.setFillColor(...COLOR.accent);
    doc.roundedRect(MARGIN, y, CONTENT_W, headerH, 3, 3, 'F');

    // Номер в белом кружке
    doc.setFillColor(...COLOR.white);
    doc.circle(MARGIN + 10, y + headerH / 2, 6, 'F');
    doc.setTextColor(...COLOR.accent);
    doc.setFont('Roboto', 'bold');
    doc.setFontSize(13);
    doc.text(String(idx + 1), MARGIN + 10, y + headerH / 2 + 1.5, {
      align: 'center'
    });

    // Название артефакта
    doc.setTextColor(...COLOR.white);
    doc.setFont('Roboto', 'bold');
    doc.setFontSize(14);
    const nameLines = wrapText(artifact.name || '', CONTENT_W - 25);
    nameLines.slice(0, 2).forEach((line, i) => {
      doc.text(line, MARGIN + 20, y + 12 + (i * 6.5));
    });

    y += headerH + 8;

    // Превью
    if (artifact.preview) {
      doc.setFillColor(...COLOR.bgLight);
      doc.setDrawColor(...COLOR.accent);
      doc.setLineWidth(0.5);

      // Левая полоса-акцент
      const previewLines = wrapText(artifact.preview, CONTENT_W - 12);
      const previewH = previewLines.length * 5.5 + 8;

      doc.setFillColor(...COLOR.bgLight);
      doc.roundedRect(MARGIN, y, CONTENT_W, previewH, 2, 2, 'F');
      doc.setFillColor(...COLOR.accent);
      doc.rect(MARGIN, y, 2, previewH, 'F');

      doc.setFont('Roboto', 'normal');
      doc.setFontSize(10);
      doc.setTextColor(...COLOR.muted);

      previewLines.forEach((line, i) => {
        doc.text(line, MARGIN + 6, y + 6 + (i * 5.5));
      });

      y += previewH + 8;
    }

    // Контент — основная часть
    doc.setFont('Roboto', 'normal');
    doc.setFontSize(11);
    doc.setTextColor(...COLOR.text);

    const contentText = stripHtml(artifact.content);
    const paragraphs = contentText.split('\n').filter(p => p.trim());

    paragraphs.forEach(paragraph => {
      const trimmed = paragraph.trim();
      if (!trimmed) return;

      ensureSpace(8);

      if (trimmed.startsWith('•')) {
        // Пункт списка
        const itemText = trimmed.substring(1).trim();

        // Маркер
        ensureSpace(7);
        doc.setFillColor(...COLOR.accent);
        doc.circle(MARGIN + 2, y - 1.5, 1.3, 'F');

        doc.setTextColor(...COLOR.text);
        drawSmartText(itemText, MARGIN + 7, CONTENT_W - 7, 5.8);
        y += 2;
      } else {
        // Обычный абзац
        doc.setTextColor(...COLOR.text);
        drawSmartText(trimmed, MARGIN, CONTENT_W, 5.8);
        y += 3;
      }
    });
  });

  // ════════════════════════════════════════════════════════
  // ── ФУТЕР И ОБНОВЛЕНИЕ КОЛОНТИТУЛОВ ─────────────────────
  // ════════════════════════════════════════════════════════
  const totalPages = doc.internal.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    doc.setFont('Roboto', 'normal');
    doc.setFontSize(8);
    doc.setTextColor(...COLOR.muted);
    doc.text(
      `Стр. ${i} из ${totalPages}`,
      PAGE_W - MARGIN, PAGE_H - 8,
      { align: 'right' }
    );
    doc.text(
      'Памятка нетворкера • Модуль 2',
      MARGIN, PAGE_H - 8
    );
  }

  // ── Сохраняем PDF ─────────────────────────────────────
  const filename = `Памятка_нетворкера_${today.toISOString().slice(0, 10)}.pdf`;
  doc.save(filename);

  showToast('✅ PDF скачан!', 'success');

  // Возвращаем кнопку в исходное состояние
  if (btn) {
    btn.disabled = false;
    btn.innerHTML = '⬇️ Скачать PDF-памятку';
  }
}
