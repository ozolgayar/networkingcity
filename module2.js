/* ----------------------------------------------------------------
   initHub()
   Навешивает обработчики на все зоны карты.
   Вызывается один раз при инициализации.
   ---------------------------------------------------------------- */
function initHub() {
  const hubMap = document.getElementById('hub-map');

  hubMap.addEventListener('click', function(e) {
    // Ищем ближайший элемент с data-zone
    const zone = e.target.closest('[data-zone]');
    if (!zone) return;

    const zoneId = zone.dataset.zone;
    handleHubZoneClick(zoneId, zone);
  });

  // Обновляем визуальное состояние зон
  refreshHubZones();
}

/* ----------------------------------------------------------------
   handleHubZoneClick(zoneId, zoneEl)
   Обрабатывает клик по зоне:
   - Играбельная зона → запуск главы
   - Энергетическая зона → пополнение энергии
   - Ловушка → показ предупреждения и трата энергии
   - Финал → переход к финальному экрану
   ---------------------------------------------------------------- */
function handleHubZoneClick(zoneId, zoneEl) {

  // ── Финальная зона ──────────────────────────────────────────
  if (zoneId === 'final') {
    showFinal();
    return;
  }

  // ── Энергетические зоны ─────────────────────────────────────
  if (ENERGY_ZONES[zoneId]) {
    const zone     = ENERGY_ZONES[zoneId];
    const uses     = m2State.energyZoneUses[zoneId] || 0;

    if (uses >= zone.maxUses) {
      showToast('Эта зона уже использована', 'warning');
      return;
    }
    if (m2State.energy >= 100) {
      showToast('Энергия уже полная!', 'warning');
      return;
    }

    // Запускаем экран пополнения
    m2State.refuelReturnChapterId = null;
    startRefuel(zoneId);
    return;
  }

  // ── Ловушки ─────────────────────────────────────────────────
  if (TRAPS[zoneId]) {
    const trap = TRAPS[zoneId];
    document.getElementById('trap-icon').textContent  = trap.icon;
    document.getElementById('trap-title').textContent = trap.title;
    document.getElementById('trap-text').textContent  = trap.text;
    openModal('modal-trap');

    // Тратим энергию ловушки
    spendEnergy(trap.energyCost);
    return;
  }

  // ── Играбельные главы ────────────────────────────────────────
  if (CHAPTERS[zoneId]) {
    const chapterIdx = chapterOrder.indexOf(zoneId);

    // Проверяем: глава уже пройдена?
    if (m2State.completedChapters.includes(zoneId)) {
      showToast('Ты уже прошёл эту главу ✅', 'success');
      return;
    }

    // Проверяем: глава заблокирована (предыдущая не пройдена)?
    if (chapterIdx > 0) {
      const prevChapter = chapterOrder[chapterIdx - 1];
      if (!m2State.completedChapters.includes(prevChapter)) {
        showToast('Сначала пройди предыдущую главу', 'warning');
        return;
      }
    }

    // Проверяем энергию
    if (m2State.energy <= 0) {
      openModal('modal-no-energy');
      return;
    }

    // Всё ок — запускаем главу
    startChapter(zoneId);
    return;
  }
}

/* ----------------------------------------------------------------
   refreshHubZones()
   Обновляет визуальные классы всех зон:
   - completed: глава пройдена → зелёный, показать ✅
   - active: следующая доступная → пульсация
   - locked: заблокирована → серая
   Вызывается после каждого изменения состояния.
   ---------------------------------------------------------------- */
function refreshHubZones() {
  chapterOrder.forEach((chId, idx) => {
    const zoneEl = document.querySelector(`[data-zone="${chId}"]`);
    if (!zoneEl) return;

    const doneEl = document.getElementById(`zone-done-${chId}`);

    // Сбрасываем все классы состояния
    zoneEl.classList.remove(
      'hub-zone-completed',
      'hub-zone-active',
      'hub-zone-locked'
    );

    if (m2State.completedChapters.includes(chId)) {
      // Пройдена
      zoneEl.classList.add('hub-zone-completed');
      if (doneEl) doneEl.style.display = 'block';

    } else {
      // Не пройдена — определяем доступность
      if (doneEl) doneEl.style.display = 'none';

      const prevDone = idx === 0 ||
        m2State.completedChapters.includes(chapterOrder[idx - 1]);

      if (prevDone) {
        zoneEl.classList.add('hub-zone-active'); // следующая доступная
      } else {
        zoneEl.classList.add('hub-zone-locked'); // заблокирована
      }
    }
  });

  // Обновляем счётчики энергетических зон
  Object.keys(ENERGY_ZONES).forEach(zoneId => {
    const zone  = ENERGY_ZONES[zoneId];
    const uses  = m2State.energyZoneUses[zoneId] || 0;
    const left  = zone.maxUses - uses;
    const usesEl = document.getElementById(zoneId + '-uses');
    const zoneEl = document.querySelector(`[data-zone="${zoneId}"]`);

    if (usesEl) usesEl.textContent = left + ' / ' + zone.maxUses;
    if (zoneEl) {
      if (left <= 0) {
        zoneEl.classList.add('hub-zone-depleted');
      } else {
        zoneEl.classList.remove('hub-zone-depleted');
      }
    }
  });

  // Показываем финальную зону если все главы пройдены
  const allDone = chapterOrder.every(id =>
    m2State.completedChapters.includes(id)
  );
  const finalZone = document.getElementById('hub-zone-final');
  if (finalZone) {
    if (allDone) {
      finalZone.classList.remove('hidden');
    } else {
      finalZone.classList.add('hidden');
    }
  }

 // Подсказка в подвале — указывает конкретную локацию
  const hint = document.getElementById('hub-footer-hint');
  if (hint) {
    if (allDone) {
      hint.textContent = 'Все главы пройдены! Жми «Подведение итогов»';
    } else {
      // Находим первую непройденную главу
      const nextChapterId = chapterOrder.find(id =>
        !m2State.completedChapters.includes(id)
      );

      // Соответствие id главы → название локации + иконка
   const locationNames = {
        chapter1: 'Кофейная стойка',
        chapter2: 'Стол с напитками',
        chapter3: 'Буфет',
        chapter4: 'Сцена',
        chapter5: 'Зал конференции',
      };

      const locName = locationNames[nextChapterId];
      if (locName) {
        hint.innerHTML = `Выбери локацию <strong>«${locName}»</strong>`;
      } else {
        hint.innerHTML = 'Кликай по подписям на карте';
      }
    }
  }
}


